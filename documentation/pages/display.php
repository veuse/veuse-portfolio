<h1>Displaying your portfolio</h1>
		
<h4>There are two ways of showing your portfolio on your site. You can either use a shortcode, or insert via a widget ( if you have installed Page Builder plugin by SiteOrigin</h4>

<code>[veuse_portfolio categories="" order="" orderby="" type="" perpage=""]</code>

<h3>Attributes</h3>
<p><b>categories:</b> Enter id's of the categories/portfolios you want to include, separated by commas. ( Required )</p>
<p><b>order:</b> Ascending ( ASC ) or descending ( DESC). Optional. Default: ASC.</p>
<p><b>orderby:</b> Order by title or date. Optional. Default: Date.</p>
<p><b>type:</b> Filtered, pagination or none. Optional. Default: filtered.</p>
<p><b>perpage:</b> How many projects to display. Optional. Default: -1 ( All ).</p>