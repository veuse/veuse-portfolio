<h1>Portfolios</h1>
<h4>Portfolio-posts are called <b>Projects</b>, and are organized in categories called portfolios.</h4>

<h3>How to create a portfolio</h3>
<ol>
	<li>Go to <a href="edit-tags.php?taxonomy=portfolio-category&post_type=portfolio" target="blank">Portfolio &raquo; Portfolios.</a></li>
	<li>Under the heading "Add New Portfolio", enter a name for your portfolio and click on the button <b>Add New Portfolio</b></li>
</ol>
